void main(){
  var month = "sept";
  switch(month){
    case "jan" : print('month is jan');
    break;
    case "feb" : print('month is feb');
    break;
    case "sept" : print('month is sept');
    break;
    case "april" : print('month is april');
    break;
    default : print('none of these');

  }
}